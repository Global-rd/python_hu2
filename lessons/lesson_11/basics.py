#real life example: építünk egy ikerházat egy adott telken

#program: a ház építési terve, nem használ semmilyen erőforrást amíg el nem kezdődik az építkezés
#process: a ház tényleges építési folyamata, már erőforrásokat használva. Ikerház miatt lehet a két oldal 2 különböző process
#thread: falazás, festés, elektronika, víz/gáz szerelés stb. egy process-en belül, vagyis egy ház-oldalon belül.